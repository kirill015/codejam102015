package codejam.africa2010.qualification.storecredit;

public class ItemValue {
	private int itemID;
	private int value;
	
	public int getItemID() {
		return itemID;
	}
	public int getValue() {
		return value;
	}
	public ItemValue(int itemID, int value) {
		super();
		this.itemID = itemID;
		this.value = value;
	}
	
	
}
